// WAJP to find the area of a circle whose radious is 2.5 cm 
class AreaOfCircle{
	public static void main(String[] args){
		double radious = 2.5;
		final double pi = 3.14;
		double area = pi*(radious*radious);

		System.out.println("Area of the circle - " + area + "cm whose radious is " + radious + "cm");
	}
}